
"""
    算数运算符
"""
print("算数运算符")
# 除法
print("11 / 3 = ", 11 / 3)

# 取整除
print("11 // 3 = ", 11 // 3)
print("11.0 // 3.0 = ", 11.0 // 3.0)

# 取余数
print("11 % 3 = ", 11 % 3)

# 求指数
print("11 ** 3 = ", 11 ** 3)

